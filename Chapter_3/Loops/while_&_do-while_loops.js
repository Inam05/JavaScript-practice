// while loop 
// working of while loop
// 1. condition is checked
// 2. if condition is true then statement is executed
// 3. condition is checked again
// 4. if condition is false then statement is not executed

let i = 10;
while (i <= 12) { // condition checked
    console.log("i =", i);  // statement run
    i++;  // increament 
}
